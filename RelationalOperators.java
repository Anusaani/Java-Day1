package com.anpc8232.day1;

public class RelationalOperators {

	public static void main(String[] args) {
		
		int x=15;
		int y=25;
		
		System.out.println(x<y);
		System.out.println(x<=5);
		System.out.println(y>50);
		System.out.println(y>=10);
		System.out.println(x==y);
		System.out.println(x!=y);
	}
}
